console.log(10 == 10); // true
console.log('Virat' == 'Virat'); // true
console.log(false == false); // true
console.log(null == null); // true

console.log([] == []); // false
console.log([10, 20] == [10, 20]); // false
console.log({} == {}); // false
console.log({ a: 10, b: 20 } == { a: 10, b: 20 }); // false
